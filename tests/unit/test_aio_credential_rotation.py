class TestAutoGeneratedPassword:
    async def test_auto_generated_password(
        self, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """Password auto-generated when new_password is None."""
        sm = AsyncMock()
        sm.call = AsyncMock(
            side_effect=[
                {"SecretString": json.dumps({"username": "admin"})},
                {"VersionId": "v-auto"},
                {
                    "SecretString": json.dumps(
                        {"username": "admin", "password": "auto"}
                    )
                },
                {
                    "VersionIdsToStages": {
                        "v-old": ["AWSCURRENT"],
                        "v-auto": ["AWSPENDING"],
                    }
                },
                {},
            ]
        )

        monkeypatch.setattr(
            mod, "async_client", lambda svc, rn=None: sm
        )
        monkeypatch.setattr(mod.asyncio, "sleep", AsyncMock())

