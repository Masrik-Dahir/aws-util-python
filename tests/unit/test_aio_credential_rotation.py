class TestDatabaseCredentialRotatorFull:
    async def test_full_rotation_success(
        self, monkeypatch: pytest.MonkeyPatch
    ) -> None:
        """Full five-step rotation with SNS, signal table, and history."""
        current_secret = json.dumps(
            {"username": "admin", "password": "old_pass"}
        )
        pending_secret = json.dumps(
            {"username": "admin", "password": "new_pass"}
        )

        sm = AsyncMock()
        sm.call = AsyncMock(
            side_effect=[
                # Step 1: GetSecretValue (current)
                {"SecretString": current_secret},
                # Step 1: PutSecretValue
                {"VersionId": "v-new-123"},
                # Step 2: GetSecretValue (pending)
                {"SecretString": pending_secret},
                # Step 5: DescribeSecret
                {
                    "VersionIdsToStages": {
                        "v-old-111": ["AWSCURRENT"],
                        "v-new-123": ["AWSPENDING"],
                    }
                },
                # Step 5: UpdateSecretVersionStage
                {},
            ]
        )

        sns = AsyncMock()
        sns.call = AsyncMock(return_value={})

        ddb = AsyncMock()
        ddb.call = AsyncMock(return_value={})

        clients: dict[str, AsyncMock] = {
            "secretsmanager": sm,
            "sns": sns,
            "dynamodb": ddb,
        }
        monkeypatch.setattr(
            mod,
            "async_client",
            lambda svc, rn=None: clients.get(svc, AsyncMock()),
        )
        monkeypatch.setattr(mod.asyncio, "sleep", AsyncMock())

        result = await mod.database_credential_rotator(
            secret_name="myapp/db",
            new_password="new_pass",
            consumer_sns_topic_arn="arn:aws:sns:us-east-1:123:topic",
            signal_table_name="rotation-signals",
            history_table_name="rotation-history",
            drain_seconds=10,
            region_name=REGION,
        )

        assert result.secret_name == "myapp/db"
        assert result.new_version_id == "v-new-123"
        assert result.validation_passed is True
        assert result.consumers_notified == 2
        assert result.drain_seconds == 10
        assert result.finalized is True
        assert result.rotation_timestamp

        # SNS was called
        sns.call.assert_awaited_once()
        # DynamoDB was called twice (signal + history)
        assert ddb.call.await_count == 2


